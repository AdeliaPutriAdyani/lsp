    protected void datatable() {
        Object[] Baris = {"ID Pelanggan", "Nama", "Jenis Kelamin", "Nomor Telepon", "Alamat"}; //nama kolom tabel di form
        tabmode = new DefaultTableModel(null, Baris);
        
        String cariItem = txt_cari.getText();
        
        try {
            String sql = "SELECT * FROM pelanggan WHERE id_pelanggan LIKE '%" + cariItem + "%' OR nama_pelanggan LIKE '%" + cariItem + "%' ORDER BY id_pelanggan ASC";
            Statement stat = connect.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[] {
                   hasil.getString("id_pelanggan"),
                   hasil.getString(2),
                   hasil.getString(3),
                   hasil.getString(4),
                   hasil.getString(5)
                });
            }
            tbl_pelanggan.setModel(tabmode);
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dipanggil" + e);
        }
    }
    
    protected void datatableriwayat(String a) {
        Object[] Baris = {"ID Transaksi", "Tanggal Transaksi", "Barang", "Jumlah", "Harga"}; //nama kolom tabel di form
        tabmode = new DefaultTableModel(null, Baris);
         
        try {
            String sql = "SELECT transaksi.id_transaksi, transaksi.tanggal_transaksi, manajemen.nama_barang, transaksi_item.jumlah, transaksi_item.harga "
                    + "FROM manajemen "
                    + "JOIN transaksi_item ON manajemen.id_barang = transaksi_item.id_barang "
                    + "JOIN transaksi ON transaksi.id_transaksi = transaksi_item.id_transaksi "
                    + "WHERE transaksi.id_pelanggan = ? "
                    + "ORDER BY transaksi.id_transaksi ASC";
            PreparedStatement stat = connect.prepareStatement(sql);
            stat.setString(1, a);
            ResultSet hasil = stat.executeQuery();
            while (hasil.next()) {
                tabmode.addRow(new Object[] {
                   hasil.getString("id_transaksi"),
                   hasil.getString("tanggal_transaksi"),
                   hasil.getString("nama_barang"),
                   hasil.getString("jumlah"),
                   hasil.getString("harga")
                });
            }
            tbl_transaksiPelanggan.setModel(tabmode);
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dipanggil" + e);
        }
    }
    
    

