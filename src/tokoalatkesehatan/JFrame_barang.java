    public void refreshTable() {
        int jumlahRow = tblManajemen.getRowCount();
        for(int i=jumlahRow-1; i>=0; i--){
            tblManajemen.removeRow(i);
        }
        
        ResultSet data = Barang.getAllManajemen();
        try {
            while(data.next()) {
                tblManajemen.addRow(new Object[]{
                    data.getString("KODE_BARANG"),
                    data.getString("NAMA_BARANG"),
                    data.getString("HARGA_BARANG"),
                    data.getString("STOK_BARANG")
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(JFrame_barang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

