    private void btn_createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_createActionPerformed
        // TODO add your handling code here:
        boolean insert = Barang.insertManajemen(tf_kodebarang.getText(), tf_namabarang.getText(), tf_harga.getText(), tf_stok.getText());
        if (insert == true) {
            refreshTable();
            tf_kodebarang.setText("");
            tf_namabarang.setText("");
            tf_harga.setText("");
            tf_stok.setText("");

            JOptionPane.showMessageDialog(this, "Data Berhasil di Insert.");
        } else {
            JOptionPane.showMessageDialog(this, "Data Gagal di Insert.");
        }
    }//GEN-LAST:event_btn_createActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        boolean update = Barang.updateManajemen(tf_kodebarang.getText(), tf_namabarang.getText(), tf_harga.getText(), tf_stok.getText());
        if(update == true) {
            refreshTable();
            tf_kodebarang.setText("");
            tf_namabarang.setText("");
            tf_harga.setText("");
            tf_stok.setText("");
            JOptionPane.showMessageDialog(this, "Data Berhasil di Update.");

            btn_create.setVisible(true);
            btn_update.setVisible(false);
            btn_delete.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Data Gagal di Update.");
        }
    }//GEN-LAST:event_btn_updateActionPerformed

